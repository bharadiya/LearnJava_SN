package LearnJava;

public class LearnLoops {
//	100 - 34 > he is failed
//	100 - 35+ > he is passed
	public static void main(String[] args) {
		int marks = 30;
		if (marks >= 35) {
			System.out.println("Student is passed");
		} else {
			System.out.println("Student is failed");
		}
	}
}

